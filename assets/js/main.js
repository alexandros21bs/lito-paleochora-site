function toggleFormCard(cardEl, toggleEl) {
	if (!cardEl || !toggleEl) {
		return;
	}
	const collapsed = cardEl.classList.toggle("is-collapsed");
	toggleEl.setAttribute("aria-expanded", String(!collapsed));
}

function openFormCard(cardEl, toggleEl) {
	if (!cardEl || !toggleEl) {
		return;
	}
	cardEl.classList.remove("is-collapsed");
	toggleEl.setAttribute("aria-expanded", "true");
}

function setInputsPlaceholders(lang) {
	const values = lang === "el"
		? { name: "Ονοματεπώνυμο", email: "Email", phone: "Τηλέφωνο", guests: "Άτομα", message: "Μήνυμα" }
		: { name: "Full Name", email: "Email", phone: "Phone Number", guests: "Guests", message: "Message" };

	document.querySelectorAll('input[name="name"]').forEach((el) => {
		el.placeholder = values.name;
	});
	document.querySelectorAll('input[name="email"]').forEach((el) => {
		el.placeholder = values.email;
	});
	document.querySelectorAll('input[name="phone"]').forEach((el) => {
		el.placeholder = values.phone;
	});
	document.querySelectorAll('input[name="guests"]').forEach((el) => {
		el.placeholder = values.guests;
	});
	document.querySelectorAll('textarea[name="message"]').forEach((el) => {
		el.placeholder = values.message;
	});
}

function applyTranslations(lang) {
	document.querySelectorAll("[data-i18n]").forEach((el) => {
		const key = el.dataset.i18n;
		if (translations[lang][key]) {
			el.textContent = translations[lang][key];
		}
	});

	if (lang === "el") {
		html.lang = "el";
		document.title = "Lito Apartments Paleochora | Direct Stay Request";
		document
			.querySelector('meta[name="description"]')
			.setAttribute(
				"content",
				"Άνετη διαμονή στην Παλαιόχωρα με άμεση επικοινωνία για αίτημα διαθεσιμότητας και κρατήσεις χωρίς μεσάζοντες."
			);

		langToggle.textContent = "GR";
	} else {
		html.lang = "en";
		document.title = "Lito Apartments Paleochora | Direct Stay Request";
		document
			.querySelector('meta[name="description"]')
			.setAttribute(
				"content",
				"Comfortable apartments in Paleochora, Crete. Direct stay requests, fast contact, and easy access to rooms, facilities and location."
			);

		langToggle.textContent = "EN";
	}

	setInputsPlaceholders(lang);

	langFields.forEach((field) => {
		field.value = lang;
	});
	localStorage.setItem("siteLang", lang);
}

function initLanguage() {
	const urlParams = new URLSearchParams(window.location.search);
	const urlLang = urlParams.get("lang");
	const savedLang = localStorage.getItem("siteLang");
	const currentLang = urlLang || savedLang || "el";
	applyTranslations(currentLang);
}

