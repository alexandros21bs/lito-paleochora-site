shareSiteButton?.addEventListener("click", async () => {
	const shareData = {
		title: document.title,
		text: "Lito Apartments Paleochora",
		url: window.location.href
	};

	if (navigator.share) {
		try {
			await navigator.share(shareData);
			return;
		} catch (_error) {
			// Ignore canceled shares and fall back to clipboard if needed.
		}
	}

	try {
		await navigator.clipboard.writeText(window.location.href);
		shareSiteButton.title = "Link copied";
		setTimeout(() => {
			shareSiteButton.title = "Share this page";
		}, 1400);
	} catch (_error) {
		window.prompt("Copy this link:", window.location.href);
	}
});

